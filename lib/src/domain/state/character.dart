import 'package:ricknmorty/src/domain/state/entity_builder.dart';

typedef CharacterEntityBuilder = TEntityBuilder<Character>;

class Character {
  final int id;
  final String name;
  final String status;
  final String species;
  final String type;
  final String gender;
  final String origin;
  final String location;
  final String image;

  Character({
    required this.id,
    required this.name,
    required this.status,
    required this.species,
    required this.type,
    required this.gender,
    required this.origin,
    required this.location,
    required this.image,
  });
}
